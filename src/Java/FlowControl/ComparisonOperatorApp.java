package Java.FlowControl;

// 비교 연산자

public class ComparisonOperatorApp {
    public static void main(String[] args) {
        System.out.println(1 > 1); // false
        System.out.println(1 == 1); // true
        System.out.println(1 < 1); // false
        System.out.println(1 >= 1); // true
    }
}
